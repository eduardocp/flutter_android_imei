      methodChannelWithName:@"flutter_android_imei"
            binaryMessenger:[registrar messenger]];
  FlutterAndroidImeiPlugin* instance = [[FlutterAndroidImeiPlugin alloc] init];
  [registrar addMethodCallDelegate:instance channel:channel];
}

